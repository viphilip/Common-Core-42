#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	size_t	size;
	char	*cpy;

	i = 0;
	size = 0;
	while (s[size])
		size++;
	cpy = malloc(sizeof(char) * size + 1);
	if (!cpy || !s)
		return (0);
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
